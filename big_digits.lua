for line in io.lines(arg[1]) do
	for k,v in ipairs(big) do
		l=""
		for num in line:gmatch("[0-9]") do
			n=tonumber(num)
			l=l..v:sub(n*5+1,n*5+5)
		end
		print(l)
	end
end
